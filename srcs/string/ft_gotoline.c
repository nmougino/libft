#include "libft.h"

char	*ft_gotoline(char *buf, int l)
{
	int		i;

	i = 0;
	if (l > 0)
	{
		while (i < l && *buf != '\0')
		{
			if (*buf == '\n')
				i++;
			buf++;
		}
	}
	return (buf);
}
