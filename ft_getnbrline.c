int		ft_getnbrline(char *buf)
{
	int		ans;
	int		i;

	ans = 0;
	i = 0;
	if (buf[i])
		ans++;
	while (buf[i])
	{
		if (buf[i] == '\n')
			ans++;
		i++;
	}
	return (ans);
}
