int		ft_strequ(char const *s1, char const *s2)
{
	int		ans;
	int		i;

	ans = 1;
	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			ans = 0;
		i++;
	}
	if (s1[i] != s2[i])
		ans = 0;
	return (ans);
}
